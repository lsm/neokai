/**
 * ToolIcon Component
 */
export function ToolIcon({
	toolName,
	size = 'md',
	className,
	animated = false,
	category,
}: ToolIconProps) {
	const config = getToolConfig(toolName);
	const colors = getToolColors(toolName);
	const sizeClass = getIconSizeClasses(size);

	// Use custom icon if provided
	if (config.icon) {
		const CustomIcon = config.icon;
		return (
			<div class={cn(sizeClass, colors.iconColor, className)}>
				<CustomIcon />
			</div>
		);
	}

	// Use default icon
	return getDefaultIcon(toolName, sizeClass, colors.iconColor, animated);
}
